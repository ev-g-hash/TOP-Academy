# Задание 3
# Подсчитать количество целых чисел в диапазоне от
# 100 до 9999 у которых все цифры разные.

a = 100
b = 10000

count = 0

for i in range(a, b):
    y = list(str(i))
    if len(y) == 3:
        if y[0] != y[1] and y[0] != y[2] and y[1] != y[2]:
            count += 1

    if len(y) == 4:
        if y[0] != y[1] and y[0] != y[2] and y[0] != y[3] and y[1] != y[2] and y[1] != y[3] and y[2] != y[3]:
            count += 1

print (f"количество целых чисел в диапазоне от ( {a} ) до ( {b - 1} ) у которых все цифры разные = {count}","\n")

