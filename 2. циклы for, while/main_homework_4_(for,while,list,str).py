
# Задание 1
# Напишите программу, которая запрашивает два
# целых числа x и y, после чего вычисляет и выводит
# значение x в степени y.

x = int(input("введите число ( х ) : "))
y = int(input("введите ( у ): "))

i = 1
z = 0           # необязательная переменная

while i <= y:
    z = x ** i
    i += 1

print(f"{x} ** {y} = {z}","\n")

# Задание 2
# Подсчитать количество целых чисел в диапазоне от
# 100 до 999 у которых есть две одинаковые цифры.

a = 100
b = 999
count = 0

while a <= b:
    a += 1
    y = list(str(a))

    if y[0] == y[1] or y[0] == y[2] or y[1] == y[2]:
        count += 1

print(f"целых чисел в диапазоне от ( 100 ) до ( 999 ) где есть по две одинаковые цифры равно: {count}","\n")


